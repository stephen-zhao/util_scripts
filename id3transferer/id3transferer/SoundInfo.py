class SoundInfo:
    def __init__(
            self,
            title="",
            artist="",
            album_title="",
            album_artist="",
            year="",
            track_num="",
            disc_num="",
            genre="",
            composer="",
            comment=""):
        self._title = title
        self._artist = artist
        self._albumTitle = album_title
        self._albumArtist = album_artist
        self._year = year
        self._trackNum = track_num
        self._discNum = disc_num
        self._genre = genre
        self._composer = composer
        self._comment = comment
